    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Boolean guardar = true;
        String mensaje = "";
        String nombre = txtNombre.getText();
        int edad = Integer.parseInt(spnEdad.getValue().toString());
        String curso = cbxCurso.getSelectedItem().toString();
        if(nombre.length()==0){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
        }
        if(edad==0){
            guardar = false;
            mensaje+="Escriba la edad, por favor\n";
        }
        if(guardar){
            ControlDatos control = new ControlDatos();
            if(curso.equals("Primer Año")) curso = "Primer Año";
            if(curso.equals("Segundo Año")) curso = "Segundo Año";
            control.insertar(nombre, edad, curso);
            txtNombre.setText("");
            spnEdad.setValue(0);
            cbxCurso.setSelectedIndex(0);
        }
        else JOptionPane.showMessageDialog(null, mensaje);
    }//GEN-LAST:event_btnGuardarActionPerformed

